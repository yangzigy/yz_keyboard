//工具
int led_p=0; //当前执行到哪个位置了
int led_set_pattern(S_LED_LIGHT *pa,int n) //按给的pattern设置，并返回延时数
{
	for(; led_p < n; led_p++)
	{
		if (pa[led_p].key==1) //若是一个组结尾
		{
			return pa[led_p++].light;
		}
		keys[ascii_index[pa[led_p].key]].led=pa[led_p].light;
	}
	led_p=0;
	return 0;
}
